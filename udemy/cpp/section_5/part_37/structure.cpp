// Identifiers, not standard keywords, created by programmers to give something meaningful a name
// i.e. #include, main, cout
//
// Operators, these are characters that perform operations when used
// i.e +, -, *, /, <<, >>
//
// Punctuation, these are certain characters used to perform other actions
// i.e. ;, (), {}
//
// Combined all these parts mesh together to create the programming language syntax

