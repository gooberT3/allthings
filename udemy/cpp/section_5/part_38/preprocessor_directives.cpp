// The C++ Preprocessor is a program that processes source code before it gets to the compiler,
// it then strips the program of all comments and replaces them with a single whitespace character,
// it then looks to execute the 'preprocessor directives' which all begin with a '#' symbol.
//
// The preprocessor DOES NOT undestand C++ source code.

// preprocessor directives
#include <iostream>

// function main
int main() {
      // display a message to the user
      std::cout << "Hello, World!\n";

      return 0;
}
